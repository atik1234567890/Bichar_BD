def classify_article(title, summary):
    text = (title + " " + summary).lower()
    scores = {}
    
    for crime_type, keywords in CRIME_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if kw.lower() in text:
                # Keywords in title get higher weight
                if kw.lower() in title.lower():
                    score += 2
                else:
                    score += 1
        if score > 0:
            scores[crime_type] = score
            
    if not scores:
        return {"is_crime_related": False, "incident_type": None, "confidence": 0}
        
    # Get the crime type with the highest score
    best_match = max(scores, key=scores.get)
    confidence = min(scores[best_match] / 3.0, 1.0) # Normalized confidence
    
    # If the score is very low (e.g. only 1 keyword in summary), mark as low confidence
    is_crime = scores[best_match] >= 2 or (scores[best_match] >= 1 and best_match in ["enforced_disappearance", "rape", "murder"])
    
    return {
        "is_crime_related": is_crime,
        "incident_type": best_match,
        "confidence": confidence,
        "all_scores": scores
    }

def extract_location(text):
    text = text.lower()
    found_thana = None
    
    # 1. Search for specific thanas first
    for thana, keywords in BD_THANAS.items():
        if any(kw.lower() in text for kw in keywords):
            found_thana = thana
            break
            
    # If we found a thana, we already know its district and division
    if found_thana and found_thana in THANA_TO_DISTRICT:
        division, district = THANA_TO_DISTRICT[found_thana]
        return {"division": division, "district": district, "thana": found_thana}

    # 2. Search for districts
    found_districts = []
    for division, districts in BD_LOCATIONS.items():
        for district in districts:
            # Avoid matching "ঢাকা" if it's just part of "ঢাকা ট্রিবিউন" or similar
            if district.lower() in text:
                # Special check for Dhaka to avoid false positives from source names
                if district.lower() in ["dhaka", "ঢাকা"]:
                    # Only count Dhaka if it's not likely part of a source name
                    source_names = ["dhaka tribune", "ঢাকা ট্রিবিউন", "dhaka post", "ঢাকা পোস্ট"]
                    if any(sn in text for sn in source_names):
                        # Check if Dhaka appears elsewhere besides the source name
                        text_without_sources = text
                        for sn in source_names:
                            text_without_sources = text_without_sources.replace(sn, "")
                        if district.lower() not in text_without_sources:
                            continue
                found_districts.append((division, district))
    
    if found_districts:
        # Prefer non-Dhaka districts if multiple are found
        non_dhaka = [d for d in found_districts if d[1].lower() not in ["dhaka", "ঢাকা"]]
        if non_dhaka:
            return {"division": non_dhaka[0][0], "district": non_dhaka[0][1], "thana": found_thana}
        return {"division": found_districts[0][0], "district": found_districts[0][1], "thana": found_thana}
        
    return {"division": None, "district": "Unknown", "thana": found_thana}

def detect_status(text):
    text = text.lower()
    if any(kw in text for kw in ["গ্রেফতার", "আটক", "arrested"]):
        return "arrested"
    if any(kw in text for kw in ["চার্জশিট", "charged"]):
        return "charged"
    if any(kw in text for kw in ["রায়", "সাজা", "verdict"]):
        return "verdict"
    return "reported"
